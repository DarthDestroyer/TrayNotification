namespace TrayNotification
{
    public enum MouseState
    {
        /// <summary>
        /// Mouse is not interacting with the area.
        /// </summary>
        None,

        /// <summary>
        /// Mouse is presently over the area.
        /// </summary>
        Over,

        /// <summary>
        /// Mouse has been depressed inside the area.
        /// </summary>
        Down
    }
}